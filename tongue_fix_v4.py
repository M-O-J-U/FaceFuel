def purge_geographic():
    """
    Remove geographic pseudo-labels that were generated by simple
    texture analysis. Keep only ones that co-occur with strong
    supporting evidence (tongue_body present + spatial variance
    confirmed on the actual image).
    """
    print("\n" + "="*60)
    print("Fix 1 — Purging bad geographic pseudo-labels")
    print("="*60)

    removed = 0
    kept    = 0

    for lbl_path in PSEUDO_LBL.glob("*.txt"):
        lines = lbl_path.read_text(errors="ignore").strip().splitlines()
        has_geo = any(l.strip().startswith("8 ") for l in lines)
        if not has_geo:
            continue

        # Check if geographic is the ONLY meaningful label (sign of false positive)
        other_cls = [l.strip().split()[0] for l in lines
                     if l.strip() and not l.strip().startswith("8 ")
                     and not l.strip().startswith("0 ")]

        # Load image and verify with stricter spatial variance check
        img_path = PSEUDO_IMG / (lbl_path.stem.lstrip("p_") + ".jpg")
        if not img_path.exists():
            # Try other extensions
            for ext in IMAGE_EXTS:
                candidate = PSEUDO_IMG / (lbl_path.stem + ext)
                if candidate.exists():
                    img_path = candidate
                    break

        verified = False
        if img_path.exists():
            img = cv2.imread(str(img_path))
            if img is not None:
                verified = verify_geographic(img)

        if not verified:
            # Remove geographic (class 8) from this label file
            new_lines = [l for l in lines if not l.strip().startswith("8 ")]
            if new_lines:
                lbl_path.write_text("\n".join(new_lines) + "\n", encoding="utf-8")
            else:
                lbl_path.write_text("", encoding="utf-8")
            removed += 1
        else:
            kept += 1

    print(f"  Removed geographic from : {removed:,} files")
    print(f"  Kept verified geographic : {kept:,} files")
    return removed


def verify_geographic(img_bgr: np.ndarray) -> bool:
    """
    Strict geographic tongue verification.
    True geographic tongue has:
    1. Very high inter-region L variance (patchy depapillation)
    2. Clear boundary between smooth patches and rough papillae
    3. NOT simply high texture or high brightness
    """
    gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY).astype(np.float32)
    lab  = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2LAB).astype(np.float32)
    L    = lab[:,:,0]
    h, w = gray.shape

    # Focus on central tongue region
    y1,y2 = int(h*0.15), int(h*0.85)
    x1,x2 = int(w*0.15), int(w*0.85)
    Lr = L[y1:y2, x1:x2]
    if Lr.size < 100: return False

    # Divide into 4x4 grid and measure L mean per cell
    grid_means = []
    rows, cols = Lr.shape
    for gy in range(4):
        for gx in range(4):
            r1 = int(rows*gy/4); r2 = int(rows*(gy+1)/4)
            c1 = int(cols*gx/4); c2 = int(cols*(gx+1)/4)
            cell = Lr[r1:r2, c1:c2]
            if cell.size > 0:
                grid_means.append(float(np.mean(cell)))

    if not grid_means: return False
    spatial_std = float(np.std(grid_means))

    # Geographic requires VERY high spatial std (>18)
    # AND some cells must be significantly lighter (depapillated patches)
    # AND some cells must be darker (remaining papillae)
    arr = np.array(grid_means)
    has_bright_patch = (arr > np.mean(arr) + 15).any()
    has_dark_patch   = (arr < np.mean(arr) - 15).any()

    return spatial_std > 18 and has_bright_patch and has_dark_patch


