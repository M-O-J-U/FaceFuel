# FIX 5: Suppress red_tongue false positives on yellow tongues
# ═══════════════════════════════════════════════════════════════

def fix_red_tongue_labels():
    """
    Remove red_tongue pseudo-labels from images that are actually
    yellow/orange tongues (high B channel = yellow, not red).
    """
    print("\n" + "="*60)
    print("Fix 5 — Suppressing red_tongue false positives")
    print("="*60)

    fixed = 0
    for lbl_path in PSEUDO_LBL.glob("*.txt"):
        lines = lbl_path.read_text(errors="ignore").strip().splitlines()
        has_red = any(l.strip().startswith("4 ") for l in lines)
        if not has_red: continue

        # Load image and check if it's actually yellow
        img_path = None
        for ext in IMAGE_EXTS:
            p = PSEUDO_IMG / (lbl_path.stem + ext)
            if p.exists(): img_path = p; break

        if not img_path: continue
        img = cv2.imread(str(img_path))
        if img is None: continue

        lab = cv2.cvtColor(img, cv2.COLOR_BGR2LAB).astype(np.float32)
        h,w = lab.shape[:2]
        y1,y2 = int(h*0.15),int(h*0.85)
        x1,x2 = int(w*0.15),int(w*0.85)
        Ac = lab[y1:y2,x1:x2,1]; Bc = lab[y1:y2,x1:x2,2]
        mA = float(np.mean(Ac)); mB = float(np.mean(Bc))

        # Remove red_tongue if B channel indicates yellow/orange (B>145)
        # OR if A channel is not actually high enough for real red
        is_yellow = mB > 145
        is_not_red = mA < 148

        if is_yellow or is_not_red:
            new_lines = [l for l in lines if not l.strip().startswith("4 ")]
            lbl_path.write_text(("\n".join(new_lines) + "\n") if new_lines else "",
                                 encoding="utf-8")
            fixed += 1

    print(f"  Removed false red_tongue labels: {fixed:,}")


