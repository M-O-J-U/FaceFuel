def detect_fissures(img_bgr: np.ndarray) -> float:
    """
    Detect tongue fissures using directional Sobel analysis.
    Fissures are strong, elongated dark lines running along tongue length.
    """
    gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY).astype(np.float32)
    h, w = gray.shape

    y1,y2 = int(h*0.10), int(h*0.90)
    x1,x2 = int(w*0.20), int(w*0.80)
    region = gray[y1:y2, x1:x2]
    if region.size < 100: return 0.0

    # Strong vertical gradient = fissure (dark line going top-to-bottom)
    sx     = cv2.Sobel(region, cv2.CV_32F, 1, 0, ksize=3)
    sy     = cv2.Sobel(region, cv2.CV_32F, 0, 1, ksize=3)

    # Fissures: vertical edges (sx large) that are consistent (not noise)
    # Use percentile to capture significant edges only
    sx_abs = np.abs(sx)
    p90_x  = float(np.percentile(sx_abs, 90))
    p90_y  = float(np.percentile(np.abs(sy), 90))

    # Strong vertical lines (fissures) have high p90 horizontal gradient
    if p90_x < 15: return 0.0  # raised from 8 — must be strong edges

    # Ratio: fissured tongue has much stronger horizontal gradient than vertical
    ratio = p90_x / (p90_y + 1e-6)

    # Also check for linear dark structures (Hough-like: count strong vertical pixels)
    strong_v = (sx_abs > p90_x * 0.8).astype(np.float32)
    col_sums = strong_v.sum(axis=0)
    # Fissure = tall column of strong pixels
    long_lines = (col_sums > region.shape[0] * 0.25).sum()
    line_density = long_lines / max(region.shape[1], 1)

    # Both ratio AND line_density must be strong — prevents noise triggering
    if ratio < 2.0 or line_density < 0.12:
        return 0.0
    score = float(np.clip((ratio - 2.0) / 2.0, 0, 0.5) +
                  np.clip((line_density - 0.12) / 0.15, 0, 0.5))
    return min(score, 1.0)


