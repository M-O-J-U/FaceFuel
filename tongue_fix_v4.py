def improve_thick_coating():
    """
    Re-run coating detection with better thresholds.
    Thick coating = high L brightness + high B (yellow) OR low L (dark coating).
    Also detects black hairy tongue as class 11.
    """
    print("\n" + "="*60)
    print("Fix 2+4 — Improving thick_coating + black_hairy_tongue detection")
    print("="*60)

    # Process ALL images including main labeled set
    target_dirs = [IMG_DIR, PSEUDO_IMG,
                   BASE / "preprocessedcropped",
                   BASE / "Tongue_color_v20"]
    all_imgs = []
    for d in target_dirs:
        if d.exists():
            for ext in IMAGE_EXTS:
                all_imgs.extend(d.rglob(f"*{ext}"))

    added_thick = 0
    added_black = 0
    updated     = 0

    for img_path in all_imgs:
        img = cv2.imread(str(img_path))
        if img is None: continue

        thick_score, black_score = analyze_coating(img)

        if thick_score < 0.35 and black_score < 0.40:
            continue

        # Determine label file location
        stem = img_path.stem
        lbl_path = LBL_DIR / f"{stem}.txt"
        if not lbl_path.exists():
            lbl_path = PSEUDO_LBL / f"{stem}.txt"

        existing = lbl_path.read_text(errors="ignore").strip() \
                   if lbl_path.exists() else ""

        new_lines = []
        if thick_score >= 0.35:
            line = "7 0.500000 0.500000 0.950000 0.950000"
            if line not in existing:
                new_lines.append(line); added_thick += 1

        if black_score >= 0.40:
            line = "11 0.500000 0.500000 0.950000 0.950000"
            if line not in existing:
                new_lines.append(line); added_black += 1

        if new_lines:
            dst_img = PSEUDO_IMG / img_path.name
            if not dst_img.exists():
                shutil.copy2(img_path, dst_img)
            lbl_out = PSEUDO_LBL / f"{dst_img.stem}.txt"
            with open(lbl_out, "a", encoding="utf-8") as f:
                f.write(("\n" if existing else "") + "\n".join(new_lines) + "\n")
            updated += 1

    print(f"  thick_coating labels added   : {added_thick:,}")
    print(f"  black_hairy_tongue labels added: {added_black:,}")
    print(f"  Files updated                : {updated:,}")


def analyze_coating(img_bgr: np.ndarray) -> tuple:
    """
    Returns (thick_coating_score, black_hairy_tongue_score).
    Uses raw image (pre-normalization) for reliable absolute thresholds.
    """
    lab  = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2LAB).astype(np.float32)
    L, A, B = lab[:,:,0], lab[:,:,1], lab[:,:,2]
    h, w = L.shape

    # Central tongue region
    y1,y2 = int(h*0.15), int(h*0.85)
    x1,x2 = int(w*0.15), int(w*0.85)
    Lc,Ac,Bc = L[y1:y2,x1:x2], A[y1:y2,x1:x2], B[y1:y2,x1:x2]
    if Lc.size < 100: return 0.0, 0.0

    mL = float(np.mean(Lc))
    mA = float(np.mean(Ac))
    mB = float(np.mean(Bc))
    std_L = float(np.std(Lc))

    # ── Thick coating (white/yellow) ──────────────────────────
    # White coating: high L (>165), neutral A/B
    white_coat = mL > 165 and abs(mA-128) < 12 and abs(mB-128) < 12
    # Yellow coating as thick: high L + high B
    yellow_thick = mL > 155 and mB > 148 and mA < 145
    # General thick: very high L (>170)
    very_bright  = mL > 170

    thick_score = 0.0
    if white_coat:   thick_score = max(thick_score, float(np.clip((mL-165)/30,0,1)))
    if yellow_thick: thick_score = max(thick_score, float(np.clip((mB-148)/25,0,1)))
    if very_bright:  thick_score = max(thick_score, float(np.clip((mL-170)/25,0,1)))

    # ── Black hairy tongue ────────────────────────────────────
    # Very dark L (<105) + brownish/neutral A + high texture
    sx    = cv2.Sobel(Lc, cv2.CV_32F, 0, 1, ksize=3)  # vertical = hair texture
    sy    = cv2.Sobel(Lc, cv2.CV_32F, 1, 0, ksize=3)
    vert_grad = float(np.mean(np.abs(sx)))
    horiz_grad = float(np.mean(np.abs(sy)))
    elongated = vert_grad > horiz_grad * 1.3   # more vertical than horizontal

    black_score = 0.0
    if mL < 105 and elongated:
        black_score = float(np.clip((105-mL)/40,0,1)) * 0.8 + \
                      float(np.clip(vert_grad/15,0,1)) * 0.2

    # Dark but not black — brownish/hairy
    if 105 <= mL < 130 and std_L < 20 and elongated:
        black_score = max(black_score, float(np.clip((130-mL)/25,0,0.7)))

    return thick_score, black_score


