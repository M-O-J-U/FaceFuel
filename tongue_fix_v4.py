def rebuild_combined():
    print("\n" + "="*60)
    print("Rebuilding TONGUE_COMBINED v4...")
    print("="*60)
    import random; random.seed(42)

    if COMBINED.exists(): shutil.rmtree(COMBINED)

    for split in ["train","val"]:
        for sub in ["images","labels"]:
            (COMBINED/split/sub).mkdir(parents=True, exist_ok=True)

    # Collect all pairs
    all_pairs = []
    for img in IMG_DIR.glob("*.*"):
        if img.suffix.lower() not in IMAGE_EXTS: continue
        lbl = LBL_DIR / f"{img.stem}.txt"
        if lbl.exists(): all_pairs.append((img, lbl, False))

    if PSEUDO_IMG.exists():
        for img in PSEUDO_IMG.glob("*.*"):
            if img.suffix.lower() not in IMAGE_EXTS: continue
            lbl = PSEUDO_LBL / f"{img.stem}.txt"
            if lbl.exists(): all_pairs.append((img, lbl, True))

    random.shuffle(all_pairs)
    sp = int(len(all_pairs)*0.8)
    train_p, val_p = all_pairs[:sp], all_pairs[sp:]

    class_counts = defaultdict(int)

    def copy_split(pairs, split):
        for img, lbl, pseudo in pairs:
            pfx = "p_" if pseudo else ""
            di  = COMBINED/split/"images"/(pfx+img.name)
            dl  = COMBINED/split/"labels"/(pfx+img.stem+".txt")
            if not di.exists(): shutil.copy2(img, di)
            if not dl.exists():
                # Update class count to 12
                shutil.copy2(lbl, dl)
            for line in lbl.read_text(errors="ignore").splitlines():
                parts = line.strip().split()
                if parts and parts[0].isdigit():
                    class_counts[int(parts[0])] += 1

    copy_split(train_p, "train")
    copy_split(val_p,   "val")

    yaml_path = COMBINED/"data.yaml"
    with open(yaml_path,"w") as f:
        yaml.dump({
            "train": str((COMBINED/"train"/"images").resolve()),
            "val":   str((COMBINED/"val"/"images").resolve()),
            "nc":    len(CLASSES),
            "names": CLASSES,
        }, f, default_flow_style=False, allow_unicode=True)

    print(f"  Train: {len(train_p):,}  Val: {len(val_p):,}")
    print(f"  Class distribution v4:")
    for i,name in enumerate(CLASSES):
        cnt = class_counts.get(i,0)
        bar = "█"*min(30,cnt//20)
        flag = ""
        if i==8 and cnt>800:  flag=" ← still high"
        if i==8 and cnt<500:  flag=" ← ✅ fixed"
        if i==11 and cnt>0:   flag=" ← ✅ new class"
        print(f"    {i:2d}  {name:<22s}  {cnt:5d}  {bar}{flag}")

    return yaml_path


def train_v4(yaml_path: Path, epochs: int = 80):
    import torch
    from ultralytics import YOLO

    device = "0" if torch.cuda.is_available() else "cpu"
    candidates = (list(Path(".").rglob("tongue_v3_improved/weights/best.pt")) +
                  list(Path(".").rglob("tongue_v2_retrain/weights/best.pt")))
    weights = str(candidates[0]) if candidates else "yolov8m.pt"
    print(f"\n  Starting from: {weights}")
    print(f"\n{'='*60}")
    print(f"Training Tongue v4  ({epochs} epochs, 12 classes)")
    print(f"{'='*60}")

    model = YOLO(weights)
    results = model.train(
        data          = str(yaml_path),
        epochs        = epochs,
        imgsz         = 640,
        batch         = 16,
        workers       = 0,
        device        = device,
        project       = str(RUN_DIR),
        name          = "tongue_v4_fixed",
        patience      = 30,
        lr0           = 8e-5,         # Low LR — fine-tuning from v3
        lrf           = 0.01,
        warmup_epochs = 3,
        box           = 7.5,
        cls           = 3.5,          # Higher cls — 12 unbalanced classes
        hsv_h         = 0.025,        # Slightly more hue variation
        hsv_s         = 0.6,
        hsv_v         = 0.4,
        fliplr        = 0.5,
        flipud        = 0.0,
        degrees       = 12,
        scale         = 0.3,
        mosaic        = 0.85,
        mixup         = 0.2,          # Mixup helps rare classes
        copy_paste    = 0.1,
        save          = True,
        exist_ok      = True,
        verbose       = True,
    )
    best = next(Path(".").rglob("tongue_v4_fixed/weights/best.pt"), None)
    map50 = results.results_dict.get("metrics/mAP50(B)", "N/A")
    print(f"\n  ✅ v4 training complete")
    print(f"  Best: {best}")
    print(f"  mAP@0.5: {map50}")
    print(f"\n  Next: re-extract features and retrain severity MLP")
    print(f"    python tongue_features.py")
    print(f"    python tongue_severity.py --train")


